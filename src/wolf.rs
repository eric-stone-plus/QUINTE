/// 母狼像素画（▀ 半块，32 行）；降级模式返回空串（省略）。
pub fn render_wolf() -> String {
    let grid: Vec<Vec<u8>> = WOLF.iter().map(|row| row.to_vec()).collect();
    crate::ui::render_half_blocks(&grid, &PALETTE, "  ")
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn grid_is_64x64() {
        assert_eq!(WOLF.len(), 64);
        assert!(WOLF.iter().all(|row| row.len() == 64));
        assert!(WOLF.iter().flatten().all(|&i| i < PALETTE.len() as u8));
    }

    #[test]
    fn render_degrades_to_empty_without_color() {
        crate::ui::force_no_color();
        assert_eq!(render_wolf(), "");
    }

    #[test]
    fn wolf_body_present() {
        // 金像素应占相当比例（母狼本体存在）
        let gold = WOLF.iter().flatten().filter(|&&i| i == 1).count();
        assert!(gold > 400, "gold pixels = {gold}");
    }
}
